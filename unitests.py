class Calc():

    def __init__(self,a,b):
        self.a = a
        self.b = b
    def jia(self):
        return self.a + self.b

    def jian(self):
        return self.a - self.b

    def chengfa(self):
        return self.a * self.b
    def chu(self):
        return self.a/self.b

